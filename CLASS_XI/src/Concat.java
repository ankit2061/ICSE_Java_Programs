import java.util.*;
public class Concat {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        System.out.println("Enter a string:");
        String s55 = sc.nextLine();
        System.out.println("Enter a string:");
        String ss1 = sc.nextLine();
        System.out.println("New String:");
        System.out.println(s55+" "+ss1);
    }
}
